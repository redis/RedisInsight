#!/usr/bin/env node
// Bump the pinned redis/docs commit used by the Command Helper data fetcher.
//
// - Reads REDIS_DOCS_COMMANDS_SHA from redisinsight/api/config/default.ts
// - Resolves the latest commit on redis/docs main
// - If unchanged, exits 0 with no output (nothing to do)
// - Otherwise fetches all 5 commands_*.json at the old and new SHAs, summarises
//   the diff, rewrites default.ts, and writes the summary to
//   commands-pin-diff.md so a workflow can use it as the PR body.
//
// Usage:
//   node scripts/bump-commands-pin.mjs           # bump and write summary
//   node scripts/bump-commands-pin.mjs --check   # only report, do not write
//
// CI integration: when GITHUB_OUTPUT is set, also emits
//   changes=true|false, new_sha=<full>, new_short_sha=<short>

import { readFile, writeFile, appendFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(__dirname, '..')
const configPath = resolve(repoRoot, 'redisinsight/api/config/default.ts')
const summaryPath = resolve(repoRoot, 'commands-pin-diff.md')

const SHA_RE = /const REDIS_DOCS_COMMANDS_SHA = '([0-9a-f]{40})'/
const FILES = [
  'commands_core',
  'commands_redisearch',
  'commands_redisjson',
  'commands_redistimeseries',
  'commands_redisbloom',
]

const checkOnly = process.argv.includes('--check')

const ghHeaders = () => {
  const h = { Accept: 'application/vnd.github+json' }
  if (process.env.GITHUB_TOKEN) {
    h.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`
  }
  return h
}

async function fetchJson(url) {
  const res = await fetch(url, { headers: ghHeaders() })
  if (!res.ok) {
    throw new Error(`GET ${url} -> ${res.status} ${res.statusText}`)
  }
  return res.json()
}

async function fetchCommandsAt(sha, file) {
  const url = `https://raw.githubusercontent.com/redis/docs/${sha}/data/${file}.json`
  const res = await fetch(url)
  if (!res.ok) throw new Error(`GET ${url} -> ${res.status}`)
  return res.json()
}

function diffFile(oldSpec, newSpec) {
  const oldKeys = new Set(Object.keys(oldSpec))
  const newKeys = new Set(Object.keys(newSpec))
  const added = [...newKeys].filter((k) => !oldKeys.has(k)).sort()
  const removed = [...oldKeys].filter((k) => !newKeys.has(k)).sort()
  const common = [...newKeys].filter((k) => oldKeys.has(k))
  const changed = common
    .filter((k) => JSON.stringify(oldSpec[k]) !== JSON.stringify(newSpec[k]))
    .sort()
  return { added, removed, changed }
}

function formatFileSection(name, d) {
  if (!d.added.length && !d.removed.length && !d.changed.length) {
    return `**${name}**: no changes\n`
  }
  let s = `**${name}**\n`
  if (d.added.length) s += `- Added (${d.added.length}): ${d.added.join(', ')}\n`
  if (d.removed.length)
    s += `- Removed (${d.removed.length}): ${d.removed.join(', ')}\n`
  if (d.changed.length)
    s += `- Schema/argument changes (${d.changed.length}): ${d.changed.join(', ')}\n`
  return s
}

async function setOutput(key, value) {
  if (!process.env.GITHUB_OUTPUT) return
  await appendFile(process.env.GITHUB_OUTPUT, `${key}=${value}\n`)
}

async function main() {
  const config = await readFile(configPath, 'utf8')
  const match = config.match(SHA_RE)
  if (!match) {
    console.error('Could not find REDIS_DOCS_COMMANDS_SHA in default.ts')
    process.exit(2)
  }
  const oldSha = match[1]

  const latest = await fetchJson('https://api.github.com/repos/redis/docs/commits/main')
  const newSha = latest.sha

  if (oldSha === newSha) {
    console.log(`Already up to date (${oldSha.slice(0, 7)})`)
    await setOutput('changes', 'false')
    return
  }

  console.log(`Comparing ${oldSha.slice(0, 7)} -> ${newSha.slice(0, 7)}`)

  let anyJsonChange = false
  const sections = []
  for (const f of FILES) {
    const [oldSpec, newSpec] = await Promise.all([
      fetchCommandsAt(oldSha, f),
      fetchCommandsAt(newSha, f),
    ])
    const d = diffFile(oldSpec, newSpec)
    if (d.added.length || d.removed.length || d.changed.length) anyJsonChange = true
    sections.push(formatFileSection(f, d))
  }

  if (!anyJsonChange) {
    console.log('SHA moved but no commands_*.json content changed — skipping bump')
    await setOutput('changes', 'false')
    return
  }

  const shortOld = oldSha.slice(0, 7)
  const shortNew = newSha.slice(0, 7)
  const compareUrl = `https://github.com/redis/docs/compare/${oldSha}...${newSha}`

  const body = [
    `Bump pinned \`redis/docs\` commit: \`${shortOld}\` → \`${shortNew}\`.`,
    '',
    `Compare: ${compareUrl}`,
    '',
    '## Command spec diff',
    '',
    ...sections,
    '',
    '_Generated by `.github/workflows/bump-commands-pin.yml`._',
  ].join('\n')

  console.log('\n' + body + '\n')

  if (checkOnly) {
    console.log('--check: not writing files')
    await setOutput('changes', 'true')
    await setOutput('new_sha', newSha)
    await setOutput('new_short_sha', shortNew)
    return
  }

  const updated = config.replace(SHA_RE, `const REDIS_DOCS_COMMANDS_SHA = '${newSha}'`)
  await writeFile(configPath, updated)
  await writeFile(summaryPath, body)

  await setOutput('changes', 'true')
  await setOutput('new_sha', newSha)
  await setOutput('new_short_sha', shortNew)
  console.log(`Wrote ${configPath} and ${summaryPath}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
